//Program to print differences between the three statements
public class Differencess
{
    public static void main(String[] args)
    {
        System.out.println("No1: "+7+7);
        System.out.println("No2: "+7+7.0);
        System.out.println("No3: "+7+'7');
    }
}
/*

Output:
No1: 77
No2: 77.0
No3: 77

 */