import java.util.*;
// Program to check whether the given two numbers are same use appropriate data type
public class CheckNo2
{
    public static void main(String[] args)
    {
        float Number1 =6.02f;
        double Number2 = 6.02d;
        if(Number1==Number2)
        {
            System.out.println("The Given two numbers are same.");
        }
        else
        {
            System.out.println("The Given two numbers are different.");
        }
    }

}

/*

Output:

The Given two numbers are different.

 */